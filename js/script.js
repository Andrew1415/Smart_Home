$("#front_lock , #Front_door_lock").click(function(){
    $("#alert").append(function(){
        return " <div id='front_lock_alert'><div class='alert alert-success' >The door has been locked</div></div>";
    });
    setTimeout(function(){
        $("#front_lock_alert").remove();
    }, 2000);

})
